shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100],
        ['седло', 1500], ['рама', 12000], ['обод', 2000], ['шатун', 200], ['седло', 2700]]

part_name = input("Введите название детали: ")

num = 0
sum = 0
for i in shop:
    if part_name == i[0]:
        num += 1
        sum += i[1]

print("Кол-во деталей —", num)
print("Общая стоимость", sum)

